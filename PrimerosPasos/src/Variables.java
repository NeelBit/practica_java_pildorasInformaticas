
public class Variables {

	public static void main(String[] args) {
		
		byte edad = 35;		// declaracion e iniciacion de variable en una linea
		
		/* la siguiente instruccion System.out imprime en la consola
		 * del sistema, en este caso imprime el valor de la variable edad
		 */
		
		System.out.println(edad);
		
		edad = 75;
		
		System.out.println(edad);
		

	}

}
